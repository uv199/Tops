// includes, startsWith, endsWith

let str = "hello world";

console.log('str.startsWith("H") :', str.startsWith("H")); // ==> false
console.log('str.startsWith("H") :', str.startsWith("h")); // ==> true
console.log('str.endsWith("H") :', str.endsWith("h")); // ==> false
console.log('str.includes("H") :', str.includes("h")); // ==> false
