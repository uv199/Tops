let obj = {
  name: "urvish",
  age: 20,
  city: "surat",
};

// to get all keys of obj
let key = Object.keys(obj);
console.log("key:", key);

// to get all value of obj
let values = Object.values(obj);
console.log("values:", values);

// to get key value pair in sub array
let entris = Object.entries(obj);
console.log("entris:", entris);
