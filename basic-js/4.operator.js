/*

JAVASCRIPT OPERATORS :-
-JavaScript operators are symbols that are used to perform operations on operands. 
For example : var sum=10+20;
Here, + is the arithmetic operator and = is the assignment operator.

------------------------------------------------------------------------------

TYPES OF OPERATORS :-

1. Arithmetic Operators
2. Comparison (Relational) Operators
3. Bitwise Operators
4. Logical Operators
5. Assignment Operators
6. Special Operators

1. ARITHMETIC OPERATORS : Arithmetic operators are used to perform arithmetic operations on the operands

->  +    =>  10+20 = 30
->  -    =>  20-10 = 10
->  *    =>  10*20 = 200
->  /    =>  20/10 = 2
->  %    =>  (Remainder)	20%10 = 0
->  ++   =>  var a=10; a++; Now a = 11
->  --   =>  var a=10; a--; Now a = 9

2. Comparison (Relational) Operators : The JavaScript comparison operator compares the two operands.

-> ==	| Is equal to	
        | 10==20 = false
-> ===	| Identical (equal and of same type)
        | 10==20 = false
-> !=	| Not equal to	
        | 10!=20 = true
-> !==	| Not Identical
	    | 20!==20 = false
-> >	| Greater than	
        | 20>10 = true
-> >=	| Greater than or equal to	
        | 20>=10 = true
-> <	| Less than	
        | 20<10 = false
-> <=	| Less than or equal to
        | 20<=10 = false

*/

// == check only value
// === check value + data type
1 == "1"; // give true
1 === "1"; // give false

